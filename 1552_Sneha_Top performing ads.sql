
CREATE DATABASE ad_performance_analysis;
CREATE TABLE Ads (
    adId SERIAL PRIMARY KEY,
    VIEWS INT NOT NULL,
    Clicks INT NOT NULL,
    COST DECIMAL(10,2) NOT NULL
);
INSERT INTO Ads (adId, VIEWS, Clicks, COST) VALUES
(1, 1000, 50, 20.5),
(2, 800, 30, 15.2),
(3, 1200, 80, 25.7),
(4, 600, 20, 10.9),
(5, 1500, 120, 40.3);

SELECT adId, ROUND((Clicks::DECIMAL / VIEWS) * 100, 1) AS CTR,
VIEWS, Clicks, COST FROM Ads
ORDER BY CTR DESC;
